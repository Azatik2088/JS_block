// Константы 
console.log(`Значение числа Пи: ${Math.PI}`);
//основание натурального логарифма
console.log(`Значение числа e: ${Math.E}`);

// Основные функции Math
console.log(`Абсолютное значение -5: ${Math.abs(-5)}`);
console.log(`Извлечение квадратного корня из 16: ${Math.sqrt(16)}`);
console.log(`Возведение 2 в степень 3: ${Math.pow(2, 3)}`);
console.log(`Округление  вверх числа 4.3: ${Math.ceil(4.3)}`);
console.log(`Округление вниз числа 4.7: ${Math.floor(4.7)}`);
console.log(`Округление числа 4.4 до ближайшего целого: ${Math.round(4.4)}`);
console.log(`Генерация случайного числа от 0 до 1: ${Math.random()}`);

//Тригонометрические функции
console.log(`Синус угла пи радиан: ${Math.sin(Math.PI)}`);
console.log(`Косинус угла пи радиан: ${Math.cos(Math.PI)}`);
console.log(`Тангенс угла пи/4 радиан: ${Math.tan(Math.PI / 4)}`);
console.log(`Синус 30 градусов: ${Math.sin(30 * Math.PI / 180)}`);
console.log(`Косинус 60 градусов: ${Math.cos(60 * Math.PI / 180)}`);
console.log(`Тангенс 45 градусов: ${Math.tan(45 * Math.PI / 180)}`);

//Логарифмические функции
console.log(`Натуральный логарифм числа e: ${Math.log(Math.E)}`);
console.log(`Логарифм 100 по основанию 10: ${Math.log10(100)}`);
console.log(`Логарифм 8 по основанию 2: ${Math.log2(8)}`);

//Другие функции
console.log(`Максимальное значение из 3, 7 и 2: ${Math.max(3, 7, 2)}`);
console.log(`Минимальное значение из 3, 7 и 2: ${Math.min(3, 7, 2)}`);
